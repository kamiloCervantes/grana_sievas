
<h5>
    <b> <span style="font-size: 16px">Código de ética</span></b> 
</h5>
<br/>
<p>1. Propósito</p>
<p>
El propósito de concienciar a cada uno de sus miembros para que el ejercicio de sus actividades de evaluación de programas académicos de grado y pregrado con fines de acreditación sea realizado siempre con apego a la honestidad, legalidad, justicia y moralidad, así como con la garantía de que su trabajo será siempre comprometido, serio, neutral e imparcial, a fin de ubicarlo al beneficio de la educación superior a fin de mejorar sus programas, la calidad de la enseñanza que imparten, así como la preparación de sus profesionistas egresados, tal como lo plasma los propósitos deGRANA-ISTEC.
</p>
<p>2. Disposiciones generales</p>
<p>
Es imprescindible que prevalezca el sentido ético en todas las actividades que desarrollen los evaluadores así como cada uno de los miembros del organismo GRANA, quienes deben estar plenamente conscientes del compromiso que contraen al recibir el reconocimiento formal por parte DE GRANA.
</p>
<p>
Este código de ética es aplicado tanto para los miembros del ámbito local, nacional e internacional, los que asumirán el compromiso de cumplirlo a partir de que obtengan el reconocimiento formal por parte del Grupo GRANA-ISTEC, ya sea como integrantes del Consejo, de las oficinas representativas, evaluadores y quien labore para este organismo evaluador, los que deberá vigilar que así sea efectivamente, para, en su caso, aplicar las sancionesdisciplinarias.
</p>

<p>   3. Comportamiento de los integrantes del organismo GRANA</p>
<p>
Los estatutos de este código guiarán la operación del GRANA en todo momento, en tanto que deberán ser observadas por todos y cada uno de sus integrantes; todos ellos deberán:
</p>
<p>
Considerar un apreciable título su pertenecer a GRANA, siendo su principal motivación el servir a la sociedad y, especialmente a la educación superior.
Tomar el compromiso y la responsabilidad en la realización de su trabajo con una disposición de beneficio a la educación superior y a las instituciones educativas de este nivel, para lo cual deberá respetar el derecho a la autonomía institucional y a la privacidad personal, así como la autonomía de discernimiento respecto a grupos de interés individual.
Considerar que faltará código y dignidad profesional cuando directa o indirectamente interponga o admita acomodos y asuntos a título personal cuando estén vinculados a las actividades de evaluación y acreditación.
Mostrar siempre ante las instituciones de educación superior y la sociedad en general una imagen favorable, de seriedad personal y de calidad moral y profesional.
Participar en las cuestiones colegiadas del organismo GRANA mediante una participación fructuosa que aporte beneficios a la calidad de la educación superior internacional.
Informar oportunamente a la Dirección GRANA cuando las condiciones externas dificulten su actividad en las responsabilidades asignadas para que sea reemplazado y no se paralicen las actividades.
Asumir que la información que poseen de GRANA es confidencial tanto para las instituciones a evaluar como para la sociedad en general y sólo podrán proporcionarla cuando le sea solicitada por las vías conducentes y solo a quien les indique el mismo organismo.
Mantener estricta confidencialidad, en particular sobre:
</p>
<ul>
    <li>
    Todos la información relacionada con las instituciones a evaluar, así como la información que se obtenga de los programas educativos evaluados con propósitos de acreditación tanto como las instituciones de educación superior privadas como a laspúblicas.
    </li>
    <li>
    Los juicios y las recomendaciones que emita GRANA para un programa evaluado, así como el contenido de las deliberaciones que para ese efecto se realizaron, será exclusividad del consejo deacreditación.
    </li>
    <li>
    El contenido de los dictámenes de GRANA mientras no hayan sido formalmente publicados por la autoridadcompetente.
    </li>
    <li>
    Evitar el participar en los organismos que dialoguen sobre asuntos relacionados con otro organismo acreditador en caso de ser así, deberá informarlo oportunamente a la autoridad deGRANA.
    </li>
</ul>
    <p>  4. Responsabilidades de los Integrantes deGRANA. Cada integrante del organismo GRANA deberá de:</p>
    <ul>
     <li>   I. Comportarse con justicia, honradez, lealtad, respeto, formalidad, discreción, honor, responsabilidad, sinceridad y en estricta disciplina de las normas legales y éticas que rigen al organismo GRANA
     </li>
     <li>   II. Emplear completamente sus conocimientos científicos y recursos técnicos en su desempeño en sus actividades laborales dentro del organismoGRANA.
     </li>
     <li>   III. Responsabilizarse de la actividad encomendada, además de indicar los alcances de su compromiso, así como de las limitaciones. Aceptar funciones sólo cuando cuente con los conocimientos necesarios y suficientes para desempeñarlas.
     </li>
     <li>   IV. Responder individualmente por los actos que, con motivo del ejercicio de sus funciones, dañen o perjudiquen a terceros, incluso cuando se realicen de manera colectiva.
     </li>
     <li>   V. Dar crédito a los compañeros, en los diferentes de niveles de la estructura organizativa GRANA por su intervención en actividades relacionadas con las evaluaciones yacreditaciones.
     </li>
     <li>   VI. Abstenerse de utilizar su posición jerárquica para que su nombre aparezca en los trabajos que haya realizado otra persona o grupo, si no participó enello.
     </li>
     <li>   VII. Observar oportunidad y puntualidad en todos los asuntos que realice como parte del ejercicio de sus funciones mientras sea miembro del organismo acreditador.
     </li>

     <li>   VIII. Ser imparcial y convenirse a la realidad al emitir una opinión o juicio en cualquier situación y ante cualquier autoridad opersona.
     </li>
     <li>   IX. No utilizar títulos o grados académicos ni atribuirlos a otros cuando sean inciertos, ni presentase como especialista en disciplinas para las cuales no esté debidamentelicenciado.
     </li>
     <li>   X. No ocultar o proteger con su puesto laboral a ninguna persona no habilitada para el ejercicio de las funciones que le son propias, ni tampoco permitir que otras personas utilicen su nombre o puesto para realizar actividades en el quehacer educativo relacionadas con la evaluación yacreditación.
     </li>
     <li>   XI. No hacer valer la autoridad que tenga por su ejercicio profesional, función administrativa o política, en el ejercicio de las funciones de evaluación y acreditación.
     </li>
     <li>   XII. No dar consultas, asesorías o indicaciones, pagadas o gratuitas, ya sea a su nombre o de terceros, de las cuales se pueda obtener un beneficio material o moral en nombre de evaluación oacreditación.
     </li>
     <li>   XIII. Rechazar pagos, remuneraciones, obsequios, premios (a menos que sean publicitarios de la institución) o dotes que se le pretenda entregar, directa o indirectamente, por el ejercicio de su función, ni mucho menos podrá solicitar o insinuar su requerimiento para efectos de la emisión de un juicio o dictamen. Deberá informar a la autoridad competente cuando se presente una situación que pueda poner en duda su probidad y la de sus compañerosevaluadores.
     </li>
     <li>   XIV. No ceder a otra persona, física o moral, las funciones o actividades que se le hayan delegado para sucumplimiento.
     </li>
     <li>   XV. No consumir bebidas alcohólicas, enervantes y cualquier tipo de sustancia durante el ejercicio de susfunciones.
     </li>
</ul>
        <p>5. Responsabilidades de los evaluadores.</p>
        <ul>
    <li>I. Acudir a las reuniones o visitas de acuerdo con la logística indicada; desempeñarse en ellas con la propiedad, el cuidado y la actividad de una persona honorable y responsable, con una actitud constructiva y presta aldialogo.
</li>

   <li> II. Exponer, al concluir una visita, reflexiones valorativas libres, equitativas y honestas sobre el estado que guarda un programa evaluado y las áreas institucionales que fueron revisadas; éstos deberán plasmarse únicamente en el informe de evaluación, nunca a los miembros de la comunidad académica evaluada, ni al personal de las instituciones o a cualquier otro organismo, persona oinstitución.
   </li>
   <li>III. Elaborar los informes de evaluación con la severidad metodológica y la ética profesional que se requieren para el cumplimiento de la responsabilidad tomada ante la institución y ante el organismo acreditador deGRANA.
   </li>
   <li> IV. Guardar en secreto profesional toda información o dato de que se tenga conocimiento acerca de la institución evaluada, el derecho a utilizarla se reserva únicamente aGRANA.
</li>
        </ul>

    <p>6. Sanciones para los integrantes de GRANA</p>
    <p>
Aparte de las sanciones civiles y penales previstas en los ordenamientos jurídicos que regulen en cada uno de los países, en particular el ejercicio de sus actividades profesionales, las infracciones a este Código de Ética pueden dar lugar apenas disciplinarias que serán validadas por el organismo GRANA y estas podrán ser:
    </p> 
    <ul>
    <li>Aviso confidencial, en mensaje reservado.</li>

    <li>Amonestación privado, en mensaje reservado.</li>

    <li>Amonestación pública, en diferentes medios.</li>

    <li>Cesación temporal de su nombramiento ante GRANA</li>

    <li>Cesación definitiva de su nombramiento ante GRANA.</li>

    <li>Denuncia ante la autoridad pública correspondiente.</li>
    </ul>